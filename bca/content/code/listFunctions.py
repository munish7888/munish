# Program to use the list functions: len(), max(), min(), list()

st = "012345"                   # Taken a string
ls = list(st)                   # Converted string into list using list()

print("List is: ", ls)          # Print new list
print("Lenght is: ", len(ls))   # Length using len() function
print("Maximun is: ", max(ls))  # Maximum using max() function
print("Minimun is: ", min(ls))  # Minimun using min() function




