# Program to check if a number is positive, negative or zero (using nested if)

num = int(input("Enter number: "))      # Input num

if num > 0:                             # If num is greater than 0
    print("Number is positive.")        # then num is positive
else:                                   # Otherwise
    if num == 0:                        # if num is equal to 0
        print("Number is Zero.")        # then num is zero
    else:                               # otherwise
        print("Number is negative.")    # num is negative   






        